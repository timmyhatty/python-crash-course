# 8-2: Favorite Book

# This program defines a function called favorite_book(title), then calls that function using an argument

# Define the function
def favorite_book(book_title):
    """This function accepts an argument for a book title, then prints a message stating that's a favorite book"""

    print(f'One of my favorite books of all time is {book_title.title()}!')

favorite_book('esperanza rising')
