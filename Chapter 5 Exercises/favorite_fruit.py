#5-7: Favorite Fruits

favorite_fruits = ['pineapples', 'bananas', 'oranges']

if 'pineapples' in favorite_fruits:
    print('You really like pineapples!')
if 'bananas' in favorite_fruits:
    print('You really like bananas!')
if 'oranges' in favorite_fruits:
    print('You really like oranges!')
if 'apples' not in favorite_fruits:
    print("It doesn't seem like you like apples!")
if 'grapes' not in favorite_fruits:
    print("It doesn't seem like you like grapes!")